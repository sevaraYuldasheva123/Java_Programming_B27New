package day05_arithmetic_operations;

public class MathPractice {

    public static void main(String[] args) {

        System.out.println(4 + 5); //-->9 -->int

        System.out.println( 4 * 5 ); //20 -->int

        System.out.println(2 * 5.5); //--> double

        System.out.println(10 / 3 ); // --> int number output 3

        System.out.println(10 / 3.0 ); // --> double number output 3.3333

        System.out.println(5 % 2); //remainder is 1

        System.out.println(4%2); // evenly devisible, no remainder --> 0



    }
}
